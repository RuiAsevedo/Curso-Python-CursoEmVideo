n1 = int(input('Digite um numero: '))
n2 = int(input('Digite outro numero: '))

if n1 > n2:
    print(f'O primeiro número digitado: {n1}, é maior que o segundo, que foi: {n2}')

elif n1 < n2:
    print(f'O segundo número digitado: {n2}, é maior que o primeiro, que foi: {n1}')

else:
    n1 == n2
    print('Ambos são iguais! ')